const makeGates = (approvedUpTo: number): TollgateGate[] =>
  ([0, 1, 2, 3, 4] as const).map((id) => ({
    id,
    name: GATE_DEFINITIONS[id].name,
    description: GATE_DEFINITIONS[id].description,
    status:
      id < approvedUpTo
        ? ('approved' as const)
        : id === approvedUpTo
          ? ('in_review' as const)
          : ('not_started' as const),
    approvedAt: id < approvedUpTo ? new Date(Date.now() - (approvedUpTo - id) * 7 * 86400000).toISOString() : undefined,
    approvedBy: id < approvedUpTo ? 'Sarah Mitchell' : undefined,
    comments: id < approvedUpTo
      ? [
          {
            id: `c-${id}-1`,
            authorName: 'Sarah Mitchell',
            authorRole: 'Procurement Director',
            content: 'Reviewed and approved. All deliverables meet the gate criteria.',
            createdAt: new Date(Date.now() - (approvedUpTo - id) * 7 * 86400000 + 3600000).toISOString(),
          },
        ]
      : [],
    requiredDocuments: GATE_DEFINITIONS[id].requiredDocuments,
    submittedDocuments: id < approvedUpTo ? GATE_DEFINITIONS[id].requiredDocuments : [],
    reviewers: [
      { id: 'r1', name: 'Sarah Mitchell', role: 'Procurement Director', email: 'smitchell@company.com' },
      { id: 'r2', name: 'James Chen', role: 'Finance Controller', email: 'jchen@company.com' },
    ],
  }));

